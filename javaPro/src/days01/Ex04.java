package days01;

public class Ex04 {
	
	public static void main(String[] args) {
		// 변수 < - > 상수
		// 변수 : 변하는 수, 값을 변경할 수 있는 기억공간
		// 상수 : 고정된 수, 값을 변경할 수 없는 기억공간
		// 자료형 변수명[=초기값];
		String name = "최지우";
		// 좌측 변수 = 우측항의 값;
		//name = "최지우";
		// The local variable name may not have been initialized
		System.out.println(name);
		name = "윤형식";
		System.out.println(name);

	}// main

}// class
