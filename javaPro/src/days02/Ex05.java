public class Ex05 {

	public static void main(String[] args) {
		
		//print() + 개행(\n) = println()
		System.out.println("이름1");
		System.out.println("이름2");
		System.out.println("이름3");
		// 드래그로 블록만들고 'ctrl + alt + 방향키' 하면 그대로 복사
		// alt + shift + a
		System.out.print("이름1\n");
		System.out.print("이름2\n");
		System.out.print("이름3");
		System.out.println();
		
		System.out.println("END");

	}

}
